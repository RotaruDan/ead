package es.eucm.ead.editor.view.builders;

import com.badlogic.gdx.scenes.scene2d.Actor;
import com.badlogic.gdx.scenes.scene2d.ui.Container;
import com.badlogic.gdx.scenes.scene2d.ui.Label;
import es.eucm.ead.editor.assets.ApplicationAssets;
import es.eucm.ead.editor.control.Controller;

/**
 * View that is shown when a project will be imported.
 */
public class ImportView implements ViewBuilder {

	private Container<Label> message;

	@Override
	public void initialize(Controller controller) {
		ApplicationAssets applicationAssets = controller.getApplicationAssets();
		message = new Container<Label>(new Label(applicationAssets.getI18N().m(
				"importing"), applicationAssets.getSkin()));
	}

	@Override
	public Actor getView(Object... args) {
		return message;
	}

	@Override
	public void release(Controller controller) {

	}

}
