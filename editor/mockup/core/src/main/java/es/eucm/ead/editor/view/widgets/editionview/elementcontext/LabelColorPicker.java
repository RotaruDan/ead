package es.eucm.ead.editor.view.widgets.editionview.elementcontext;

import com.badlogic.gdx.graphics.Color;
import com.badlogic.gdx.scenes.scene2d.InputEvent;
import com.badlogic.gdx.scenes.scene2d.InputListener;
import com.badlogic.gdx.scenes.scene2d.ui.Label;
import com.badlogic.gdx.scenes.scene2d.ui.Skin;

import es.eucm.ead.editor.control.Controller;
import es.eucm.ead.editor.control.actions.model.ChangeSelectionText;
import es.eucm.ead.editor.view.widgets.editionview.draw.ColorPicker;

public class LabelColorPicker extends ColorPicker {

	private Label label;

	public LabelColorPicker(final Controller controller, boolean vertical,
			float padding, float size, Skin skin) {
		super(true, padding, size, skin);
		picker.addListener(new InputListener() {

			@Override
			public boolean touchDown(InputEvent event, float x, float y,
					int pointer, int button) {
				return true;
			}

			@Override
			public void touchUp(InputEvent event, float x, float y,
					int pointer, int button) {
				controller.action(ChangeSelectionText.class, label.getText()
						.toString(), picker.getPickedColor());
			}
		});
	}

	@Override
	protected void colorChanged(Color newColor) {
		if (label != null) {
			label.getStyle().fontColor.set(newColor);
		}
	}

	public void setLabel(Label label) {
		this.label = label;
		picker.updatePosition(label.getStyle().fontColor);
	}

}
